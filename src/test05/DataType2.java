package test05;

public class DataType2 {

	public static void main(String[] args) {
		// java는 Math Class 제공 
		//올림 , 반올림, 내림
		//ceil, round, floor
		
		double d1 = 1.23456789;
		
		System.out.println(Math.ceil(d1));
		System.out.println(Math.round(d1));
		System.out.println(Math.floor(d1));
		
	}
	
	
	
}
